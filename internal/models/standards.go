// 标准类型常量
const (
	StandardCN  = "cn"  // 中国标准
	StandardWHO = "who" // WHO标准
)

// GetLMSData 获取指定年龄和性别的LMS数据
func GetLMSData(standardType string, ageInMonths int, gender string) *LMSData {
	sex := 0
	if gender == "male" {
		sex = 1
	}

	// WHO标准只支持0-60月龄
	if standardType == StandardWHO && ageInMonths > 60 {
		ageInMonths = 60
	}

	for _, data := range WHOHeightStandards {
		if data.Age == ageInMonths && data.Sex == sex {
			return &data
		}
	}

	return nil
}
